int ft_atoi(char *str)
{
	int i;
	int neg;
	int num;

	i = 0;
	num = 0;
	neg = 1;
	while (str[i] < 33)
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			neg = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		num = num * 10 + str[i] - '0';
		i++;
	}
	return (num * neg);
}