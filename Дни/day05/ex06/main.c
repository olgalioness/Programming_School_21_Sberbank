int		ft_strcmp(char *s1, char *s2)
{
	int i;

	i = 0;
	while (s1[i] != '\0' && s2[i] != '\0')	// цикл будет идти, пока i не дойдет до \0
	{	// У каждого символа, согласно ASCII таблицы есть DEC номер. Например у 'a' dec = 65
		if ((s1[i] - s2[i]) != 0)	// ((s1[a]=65) - (s2[b]=66) => -1) не равно != 0, а значит буквы разные
		{
			return (s1[i] - s2[i]); // то возвращаем это значение => -1, на этом алгоритм заканчивается делая return
		}
		i++; // Если буквы совпадают и при вычитании дают 0, то переходим на следующий.
	}
	return (s1[i] - s2[i]);	// И вот мы перебрали все цифры и они все совпали а значит возвращаем 0.
}

