// https://github.com/HalimHamidov/C-Exam-Alone-In-The-Dark---Beginner/blob/656739928e58aece3caba00e04daf88e16b8455f/Level02/ft_strcmp.c

// Аргументы:
// str1, str2 – указатели на сравниваемые строки.
//
// Возвращаемое значение:
// 0 – если сравниваемее строки идентичны.
// Положительное число – если строки отличаются и код первого отличающегося символа в строке str1 больше кода символа на той же позиции в строке str2.
// Отрицательное число – если строки отличаются и код первого отличающегося символа в строке str1 меньше кода символа на той же позиции в строке str2.
//
// Описание:
// Функция побайтно сравнивает коды символов двух строк, на которые указывают аргументы функции. Сравнение продолжается до встречи первого отличающегося
// символа или пока не будут проверены все символы строк. Если все символы строк совпали, то возвращается 0. Если при сравнении встретились отличающиеся
// символы, то сравнение прекращается и возвращается отрицательное или положительное число, в зависимости от того больше или меньше код символа строки str1,
// кода символа строки str2 на той же позиции (см. возвращаемые значения). Если строки разной длины, то возвращается положительное число, если строка str1
// длиннее строки str2, или отрицательное число, если строка str2 длиннее строки str1.

