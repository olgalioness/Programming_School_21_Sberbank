#include "ft_list.h"

void	ft_fn(char **arr, int **arr2)
{
	int i;
	int j;
	int x;
	int y;
	int n;

	i = 0;
	n = 0;
	while (i < g_h)
	{
		j = 0;
		while (j < g_l)
		{
			if (arr2[i][j] > n)
			{
				n = arr2[i][j];
				x = j;
				y = i;
			}
			j++;
		}
		i++;
	}
	free(arr2);
	ft_write(arr, x, y, n);
}
