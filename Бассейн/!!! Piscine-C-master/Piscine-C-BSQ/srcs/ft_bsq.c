#include "ft_list.h"

int	**bsq(int **arr2)
{
	int i;
	int j;
	int m;

	i = 1;
	while (i < g_h)
	{
		j = 1;
		while (j < g_l)
		{
			if ((arr2[i][j] != 0) && (arr2[i - 1][j] != 0) && (arr2[i][j - 1]\
			!= 0) && (arr2[i - 1][j - 1] != 0))
			{
				m = arr2[i][j - 1];
				if (arr2[i - 1][j] <= m)
					m = arr2[i - 1][j];
				if (arr2[i - 1][j - 1] <= m)
					m = arr2[i - 1][j - 1];
				arr2[i][j] = m + 1;
			}
			j++;
		}
		i++;
	}
	return (arr2);
}
