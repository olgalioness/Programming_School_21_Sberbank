#include "ft_list.h"

void	ft_len(char *buf, int ret)
{
	int i;
	int f;
	int l;
	int h;

	i = 0;
	l = 0;
	h = 0;
	f = 0;
	while (i < ret)
	{
		if (buf[i] != '\n' && h == 1)
			l = l + 1;
		if (buf[i] == '\n')
			h = h + 1;
		i++;
	}
	g_h = h - 1;
	g_l = l;
	ft_1_to_2(buf, 0, 0);
}
