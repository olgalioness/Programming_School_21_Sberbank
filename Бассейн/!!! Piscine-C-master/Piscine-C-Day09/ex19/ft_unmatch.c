int	ft_unmatch(int *tab, int length)
{
	int i;
	int s;
	int j;

	i = 0;
	s = 0;
	while (i < length)
	{
		j = i + 1;
		while (j < length)
		{
			if (tab[i] == tab[j])
			{
				s = s + 1;
				j++;
			}
		}
		if (s % 2 != 0)
			return (i);
		i++;
	}
	return (0);
}
