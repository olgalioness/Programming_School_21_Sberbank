char*ft_strlowcase(char *str)
{
	int i;
	int r;

	i = 0;
	r = 'A' - 'a';
	while (str[i] != '\0')
	{
		if (str[i] >= 'A' && str[i] <= 'Z')
			str[i] = str[i] - r;
		i++;
	}
	return (str);
}
