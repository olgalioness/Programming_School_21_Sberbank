#include <stdio.h>

int	ft_atoi(char *str)
{
	int a;
	int m;
	int i;

	a = 0;
	m = 1;
	i = 0;
	while (str[i] == '\t' || str[i] == '\n' || str[i] == '\v' \
	|| str[i] == '\f' || str[i] == '\r' || str[i] == ' ')
		i++;
	if (str[i] == '-' && str[i + 1] >= '0' && str[i + 1] <= '9')
		m = -1;
	if ((str[i] == '-' || str[i] == '+') && (str[i + 1] >= '0' \
	&& str[i + 1] <= '9'))
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		a = a * 10 + (str[i] - '0');
		i++;
	}
	return (a * m);
}
