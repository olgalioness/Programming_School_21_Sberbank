char	*ft_strcapitalize(char *str)
{
	int i;
	int r;
	int f;

	i = 0;
	r = 'A' - 'a';
	f = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 'a' && str[i] <= 'z' && f == 0)
		{
			str[i] = str[i] + r;
			f = 1;
		}
		i++;
		if (str[i] != '\0' && (str[i] < 'a' || str[i] > 'z'))
			f = 0;
	}
	return (str);
}
