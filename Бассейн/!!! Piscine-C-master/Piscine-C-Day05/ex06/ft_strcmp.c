int	ft_strcmp(char *s1, char *s2)
{
	int i;
	int f;

	f = 0;
	i = 0;
	while (s1[i] != '\0' || s2[i] != '\0' || f == 0)
	{
		f = s1[i] - s2[i];
		i++;
	}
	return (f);
}
