void	ft_putchar(char c);

void	ft_putnbr(int nb)
{
	int		d;
	char	a;
	int		b;

	d = 1000000000;
	a = ' ';
	b = 0;
	if (nb == 0)
		ft_putchar('0');
	if (nb < 0)
		ft_putchar('-');
	while (d != 0)
	{
		if ((nb / d != 0) || b == 1)
		{
			b = 1;
			if (nb / d < 0)
				a = ((nb / d) * (-1));
			else
				a = nb / d;
			ft_putchar('0' + a);
			nb = nb % d;
		}
		d = d / 10;
	}
}
