char	*ft_strupcase(char *str)
{
	int i;
	int r;

	i = 0;
	r = 'A' - 'a';
	while (str[i] != '\0')
	{
		if (str[i] >= 'a' && str[i] <= 'z')
			str[i] = str[i] + r;
		i++;
	}
	return (str);
}
