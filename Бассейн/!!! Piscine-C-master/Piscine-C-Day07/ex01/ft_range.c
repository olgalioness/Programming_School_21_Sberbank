#include <stdlib.h>

int	*ft_range(int min, int max)
{
	int *p;
	int i;

	if ((max <= min) || (min < -2147483648) || (max > 2147483647) ||\
		(max - min == 2147483647))
		return (NULL);
	p = (int*)malloc(sizeof(*p) * (max - min));
	i = 0;
	while (i < (max - min))
	{
		p[i] = min + i;
		i++;
	}
	return (p);
}
