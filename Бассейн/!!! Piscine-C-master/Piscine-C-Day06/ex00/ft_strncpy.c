char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	unsigned int		i;
	int					b;

	b = 0;
	i = 0;
	while (i < n)
	{
		if (src[i] != '\0' && b == 0)
			dest[i] = src[i];
		else
		{
			dest[i] = '\0';
			b = 1;
		}
		i++;
	}
	return (dest);
}
