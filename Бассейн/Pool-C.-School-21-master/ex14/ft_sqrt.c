int		ft_sqrt(int nb)
{
	int i;
	int div_nb;

	i = 0;
	div_nb = nb / 2;
	if (nb == 1)
		return (1);
	while (i <= div_nb)
	{
		if ((i * i) == nb)
			return (i);
		i++;
	}
	return (0);
}
