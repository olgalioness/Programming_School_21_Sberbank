#include <stdlib.h>

char	*ft_strdup(char *src)
{
	char	*d;
	int		l;

	l = 0;
	while (*(src + l))
		l++;
	d = (char*)malloc(sizeof(*d) * (l + 1));
	l = 0;
	while (*(src + l))
	{
		*(d + l) = *(src + l);
		l++;
	}
	*(d + l) = '\0';
	return (d);
}
