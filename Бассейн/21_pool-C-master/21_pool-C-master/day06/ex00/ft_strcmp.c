int		ft_strcmp(char *s1, char *s2)
{
	int i;
	int res;

	i = 0;
	res = 0;
	while (res == 0)
	{
		if (s1[i] > s2[i])
			res = 1;
		else if (s1[i] < s2[i])
			res = -1;
		if (s1[i] == '\0')
			break ;
		i++;
	}
	return (res);
}
