char *ft_strcpy(char *dest, char *src)
{
	int		i;
	char	*z;

	i = 0;
	z = dest;
	while (dest[i] != '\0' && src[i] != '\0')
	{
		*(dest + i) = *(src + i);
		i++;
	}
	while (dest[i] != '\0')
	{
		dest[i] = '\0';
	}
	return (z);
}
