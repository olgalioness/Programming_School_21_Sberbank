int	ft_iterative_factorial(int nb)
{
	int c;

	c = nb;
	if (nb < 0 || nb > 12)
		c = 0;
	if (nb == 0)
		c = 1;
	if (nb > 0 && nb <= 12)
	{
		while (nb > 1)
			c *= --nb;
	}
	return (c);
}
