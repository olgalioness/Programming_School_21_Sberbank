int	ft_sqrt(int nb)
{
	int tr;

	tr = 1;
	while (tr > 0)
	{
		if (tr * tr == nb)
		{
			return (tr);
			tr = 0;
		}
		else if (tr * tr > nb)
		{
			return (0);
			tr = 0;
		}
		else
		{
			tr += 1;
		}
	}
}
