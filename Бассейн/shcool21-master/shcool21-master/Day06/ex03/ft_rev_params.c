void	ft_putchar(char c);

int		main(int argc, char **argv)
{
	int j;

	argc -= 1;
	while (argc > 0)
	{
		j = 0;
		while (argv[argc][j])
		{
			ft_putchar(argv[argc][j]);
			j++;
		}
		ft_putchar('\n');
		argc--;
	}
	return (0);
}
