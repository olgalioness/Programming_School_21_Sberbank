char	*ft_rot42(char *str)
{
	int i;
	int j;

	i = 0;
	while (str[i])
	{
		if (str[i] <= 'Z' && str[i] >= 'A')
		{
			j = str[i] + 42;
			while (j > 'Z')
				j -= 26;
			str[i] = j;
		}
		else if (str[i] <= 'z' && str[i] >= 'a')
		{
			j = str[i] + 42;
			while (j > 'z')
				j -= 26;
			str[i] = j;
		}
		++i;
	}
	return (str);
}
