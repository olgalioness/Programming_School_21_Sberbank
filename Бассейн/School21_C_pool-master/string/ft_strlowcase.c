char	*ft_strlowcase(char *str)
{
	char	*i;
	int		j;

	i = str;
	j = 0;
	while (str[j])
	{
		if (str[j] >= 'A' && str[j] <= 'Z')
			str[j] = str[j] - 'A' + 'a';
		j++;
	}
	str = i;
	return (str);
}
