char	*ft_strupcase(char *str)
{
	char	*i;
	int		j;

	i = str;
	j = 0;
	while (str[j])
	{
		if (str[j] >= 'a' && str[j] <= 'z')
			str[j] = str[j] + 'A' - 'a';
		j++;
	}
	str = i;
	return (str);
}
