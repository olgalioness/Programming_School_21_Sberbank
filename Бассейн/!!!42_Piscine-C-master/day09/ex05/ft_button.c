int		ft_button(int i, int j, int k)
{
	if (j > i && i > k || k > i && i > j)
		return (i);
	else if (i > j && j > k || k > j && j > i)
		return (j);
	else if (i > k && k > j || j > k && k > i)
		return (k);
	else
		return (0);
}
