char	*ft_strlowcase(char *str)
{
	int count;

	count = 0;
	while (*str != '\0')
	{
		if (*str >= 'A' && *str <= 'Z')
		{
			*str = *str - 'A' + 'a';
			str++;
		}
		else
			str++;
		count++;
	}
	return (str - count);
}
