char	*ft_strcat(char *dest, char *src)
{
	int i;

	i = 0;
	while (*dest != '\0')
	{
		dest++;
		i++;
	}
	while (*src != '\0')
	{
		*dest = *src;
		src++;
		dest++;
		i++;
	}
	dest[i] = '\0';
	return (dest - i);
}
