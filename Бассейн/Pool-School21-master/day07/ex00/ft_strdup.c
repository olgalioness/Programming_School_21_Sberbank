#include <stdlib.h>

int		ft_strlen(char *str)
{
	int count;

	count = 0;
	while (str[count] != '\0')
		count++;
	return (count);
}

char	*ft_strcpy(char *dest, char *src)
{
	int i;

	i = 0;
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}

char	*ft_strdup(char *src)
{
	char	*ans;
	int		len;

	len = ft_strlen(src);
	ans = (char*)malloc(sizeof(char) * (len + 1));
	if (ans == NULL)
		return (0);
	ans = ft_strcpy(ans, src);
	return (ans);
}
