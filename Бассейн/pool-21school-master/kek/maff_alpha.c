#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

int main()
{
	char c;

	c = 'A';
	while (c <= 'Z')
	{
		if (c % 2 == 1)
			ft_putchar(c + 32);
		else
			ft_putchar(c);
		c++;
	}
}
