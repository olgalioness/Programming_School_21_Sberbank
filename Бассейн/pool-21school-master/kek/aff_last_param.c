#include <unistd.h>

void	ft_puts(char *c)
{
	while (*c)
	{
		write(1, &(*c), 1);
		c++;
	}
}

int main(int argc, char **argv)
{
	if (argc > 1)
		ft_puts(argv[argc - 1]);
	ft_puts("\n");
}
