int		ft_str_is_numeric(char *str)
{
	int i;
	int len;

	len = 0;
	i = 0;
	while (*str)
	{
		if (*str >= '0' && *str <= '9')
			i++;
		len++;
		str++;
	}
	if (len == i)
		return (1);
	else
		return (0);
}
