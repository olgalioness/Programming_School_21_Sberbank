int		ft_str_is_alpha(char *str)
{
	int i;
	int len;

	len = 0;
	i = 0;
	while (*str)
	{
		if ((*str >= 'A' && *str <= 'Z') || (*str >= 'a' && *str <= 'z'))
			i++;
		len++;
		str++;
	}
	if (len == i)
		return (1);
	else
		return (0);
}
