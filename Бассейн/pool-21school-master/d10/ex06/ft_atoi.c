int		ft_atoi(char *str)
{
	int i;
	int n;

	n = 0;
	i = 1;
	while (*str == ' ' || *str == '\n' || *str == '\t' || *str == '\v'
			|| *str == '\b' || *str == '\r' || *str == '\f' || *str == '0')
		str++;
	if (*str == '-')
	{
		i = -1;
		str++;
	}
	if (*str == '+')
		str++;
	while (*str >= '0' && *str <= '9')
	{
		n = 10 * n + (*str - '0');
		str++;
	}
	return (i * n);
}
