unsigned char reverse_bits(unsigned char octet)
{
	return (((octet & 128) >> 7) | ((octet & 64) >> 5)
			| ((octet & 32) >> 3) | ((octet & 16) >> 1)
			| ((octet & 8) << 1) | ((octet & 4) << 3)
			| ((octet & 2) << 5) | ((octet & 1) << 7));
}
