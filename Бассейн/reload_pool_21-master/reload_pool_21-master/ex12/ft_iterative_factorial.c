int	ft_iterative_factorial(int nb)
{
	int i;
	int res;

	i = 1;
	res = 1;
	if (nb >= 0 && nb <= 12)
	{
		if (nb == 0)
			return (1);
		while (i <= nb)
		{
			res *= i;
			i++;
		}
		return (res);
	}
	return (0);
}
