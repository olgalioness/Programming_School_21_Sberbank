int	ft_sqrt(int nb)
{
	int t;
	int sqr;

	if (nb > 0)
	{
		sqr = nb / 2;
		t = sqr;
		sqr = (t + (nb / t)) / 2;
		while ((t - sqr) > 1)
		{
			t = sqr;
			sqr = (t + (nb / t)) / 2;
		}
		if (sqr * sqr == nb)
			return (sqr);
		else
			return (0);
	}
	return (0);
}
